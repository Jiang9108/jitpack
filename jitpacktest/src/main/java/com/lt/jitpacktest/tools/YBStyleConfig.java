package com.lt.jitpacktest.tools;

public class YBStyleConfig {
    private String titleText;
    private int titleBackColor;
    private int titleTextColor;
    private int titleBackIcon;
    private int navigateTextColor;

    public void setTitleText(String titleText) {
        this.titleText = titleText;
    }

    public void setTitleBackColor(int titleBackColor) {
        this.titleBackColor = titleBackColor;
    }

    public void setTitleTextColor(int titleTextColor) {
        this.titleTextColor = titleTextColor;
    }

    public void setTitleBackIcon(int titleBackIcon) {
        this.titleBackIcon = titleBackIcon;
    }

    public void setNavigateTextColor(int navigateTextColor) {
        this.navigateTextColor = navigateTextColor;
    }

    public String getTitleText() {
        return titleText;
    }

    public int getTitleBackColor() {
        return titleBackColor;
    }

    public int getTitleTextColor() {
        return titleTextColor;
    }

    public int getTitleBackIcon() {
        return titleBackIcon;
    }

    public int getNavigateTextColor() {
        return navigateTextColor;
    }
}
